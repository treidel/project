const std::string to_string(const uint8_t *data_p, size_t length)
{
    char buffer[(2 * length) + 1];
    for (int counter = 0; counter < length; counter++)
    {
        uint8_t value = *(data_p + counter);
        snprintf(buffer + (2 * counter), 3, "%02X", value);
    }
    return buffer;
}


